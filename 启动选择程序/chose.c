void touch_init(){
	fd_touch = open("/dev/input/event0",O_RDONLY);
    if (-1 == fd_touch)
    {
        perror("open touch error");
    }
} 
int  touch_mode()
{
	struct input_event ev;
    ssize_t res;
	res = read(fd_touch, &ev, sizeof(ev));
    if (res == -1)
    {
        perror("read touch data error");
        close(fd_touch);
    }

    if(ev.type == EV_ABS)
    {
        if(ev.code == ABS_X)
        {
            x = ev.value;
            x = x*799/1023.0;
        }
        else if(ev.code == ABS_Y)
        {
            y = ev.value;
            y = y*479/599.0;
        }
    }
    else if (ev.type == EV_KEY && ev.code == BTN_TOUCH)
    {
        if(ev.value == 1)
        {
            printf("pressed!\n");
            x0 = x;
            y0 = y;
        }
        else if (ev.value == 0)
        {
            printf("released!\n");
            printf("(%d,%d)->(%d,%d)\n",x0,y0,x,y);
            return 1;
        }
    }
    return 0;
}
int lcdInit()
{
    //寮甯х茶惧浠
    fd_lcd = open("/dev/fb0",O_RDWR);
    if (-1 == fd_lcd)
    {
        perror("open lcd error");
        return -1;
    }
    //灏
    pmap = mmap(NULL, size_fb0, PROT_READ|PROT_WRITE, MAP_SHARED, fd_lcd, 0);
    if (pmap == MAP_FAILED)
    {
        perror("mmap error");
        close(fd_lcd);
        return -1;
    }
    return 0;
}
void lcdClose()
{
    //瑙ｉゆ灏
    munmap(pmap,size_fb0);
    //抽甯х茶惧浠
    close(fd_lcd);
}
