void lcdGetBMP(const char* pic_path,int x0,int y0,int mode)
{
    //寮剧浠
    int fd_pic = open(pic_path,O_RDONLY);
    if (-1 == fd_pic)
    {
        perror("open pic error");
        return;
    }
    
    //璇诲剧灞(榄般绱扮绉诲板瀹藉害楂搴叉繁)
    short MS,depth; //榄帮叉繁
    read(fd_pic,&MS,2);
    //ゆ浠舵涓bmp剧锛濡涓bmp剧灏辩
    if (0x4d42 != MS)
    {
        printf("this pic is not bmp!\n");
        close(fd_pic);
        return;
    }
    lseek(fd_pic,0x0A,SEEK_SET);
    read(fd_pic,&offset,4);
    lseek(fd_pic,0x12,SEEK_SET);
    read(fd_pic,&width,4);
    read(fd_pic,&height,4);
    lseek(fd_pic,2,SEEK_CUR);
    read(fd_pic,&depth,2);
    printf("%d %d %d",width,height,depth);
    //拌诲扮版
    //printf("MS=0x%x,offset=0x%x,width=%d,height=%d,depth=%d\n",MS,offset,width,height,depth);

    //璇诲剧绱扮版
    int full_bytes = (4-(width*depth/8)%4)%4;  //姣涓琛濉瀛=(4-杩涓琛澶瀛)%4
    int total_full = full_bytes*abs(height);  //荤濉瀛=姣涓琛濉瀛*琛
    int buf_size = abs(height)*width*depth/8+total_full;    //绱扮澶у=绱圭涓x姣涓绱归插肩瀛+濉诲
    unsigned char color_buf[buf_size];   //棰叉扮锛ㄦヤ瀛绱圭棰插
    lseek(fd_pic,offset,SEEK_SET);
    read(fd_pic,color_buf,buf_size);
    //涓涓涓涓绱瑰版剧ず剧(灏剧涓绱逛涓涓扮诲板骞涓)
    unsigned char *p = color_buf;   //涓轰逛究璁块版锛杩pヨ块
    unsigned char a,r,g,b;  //4涓棰插
    for (int y = 0; y < abs(height); y++)
    {
        for (int x = 0; x < width; x++)
        {
            b = *p++;
            g = *p++;
            r = *p++;
            if (depth == 32)
            {
                a = *p++;
            }
            else if (depth == 24)
            {
                a = 0x00;
            }

            color[y][x] = a << 24 | r << 16 | g << 8 | b;
        }
        //璺宠杩涓琛㈢濉瀛
        p += full_bytes;
    }
    if(mode==1)
    {
	   	for(int y=0;y<height;y++)
		{
			for(int x=0;x<width;x++)
			{
	            if (height > 0)
	            {
	                lcdDrawPoin(x+x0,y+y0,color[height-y-1][x]);
	            }
	            else
	            {
	                lcdDrawPoin(x+x0,height-y+1+y0,color[height-y-1][x]);
	            }
			}
			usleep(2000);		
		}
	}
	else if(mode==2)
	{
		for(int r=0;r<width+height;r++)
		{
			for(int y=0;y<height&&y<=r;y++)
			{
				for(int x=0;x<width&&x<=r;x++)
				{
					if(x+y==r)
					{
						if (height > 0)
			            {
			                lcdDrawPoin(x+x0,y+y0,color[height-y-1][x]);
			            }
			            else
			            {
			                lcdDrawPoin(x+x0,height-y+1+y0,color[height-y-1][x]);
			            }
					}
				}
			}
			usleep(2000);
		}
	}
	else if(mode==3)
	{
		unsigned int color1[height][width];
		unsigned int a,r,g,b;
		for(int z=0;z<101;z++)
		{
			for(int y=0;y<height;y++)
			{
				for(int x=0;x<width;x++)
				{
					a=color[y][x]&0xff000000;
					a=(z*a/100)&0xff000000;
					r=color[y][x]&0x00ff0000;
					r=(z*r/100)&0x00ff0000;
					g=color[y][x]&0x0000ff00;
					g=(z*g/100)&0x0000ff00;
					b=color[y][x]&0x000000ff;
					b=(z*b/100)&0x000000ff;
					color1[y][x]=a+r+g+b;
					if (height > 0)
			        {
			            lcdDrawPoin(x+x0,y+y0,color1[height-y-1][x]);
			        }
			        else
			        {
			            lcdDrawPoin(x+x0,height-y+1+y0,color1[height-y-1][x]);
			        }
				}
			}
			usleep(1);
		}
	}
	else if(mode==4)
	{
		for(int x=0;x<width;x++)
		{
			for(int y=0;y<height;y++)
			{
				if (height > 0)
	            {
	                lcdDrawPoin(x+x0,y+y0,color[height-y-1][x]);
	            }
	            else
	            {
	                lcdDrawPoin(x+x0,height-y+1+y0,color[height-y-1][x]);
	            }
			}
		}
	}
	else if(mode==5)
	{
		for(int x=width-1;x>0;x++)
		{
			for(int y=0;y<height;y++)
			{
				if (height > 0)
	            {
	                lcdDrawPoin(x+x0,y+y0,color[height-y-1][x]);
	            }
	            else
	            {
	                lcdDrawPoin(x+x0,height-y+1+y0,color[height-y-1][x]);
	            }
			}
		}
	}
	else
	{
		for(int y=0;y<height;y++)
		{
			for(int x=0;x<width;x++)
			{
	            if (height > 0)
	            {
	                lcdDrawPoin(x+x0,y+y0,color[height-y-1][x]);
	            }
	            else
	            {
	                lcdDrawPoin(x+x0,height-y+1+y0,color[height-y-1][x]);
	            }
			}
					
		}
	}
	close(fd_pic);
}

void lcdDrawWord(int x0,int y0,char *pword,unsigned int color)
{
    //妯＄锛涓涓瀛涓涓瀛版垮烘ュ
    for (int h = 0; h < word_height; h++)    //涓辨杩涔澶琛版
    {
        for (int w = 0; w < word_width/8; w++) //姣涓琛WORD_W/8涓瀛版
        {
            for (int i = 7; i >= 0; i--) //姣涓涓瀛8涓绱圭舵锛寰8娆
            {
                if(*(pword+h*word_width/8+w)>>i & 0x01)
                {
                    //璇ヤ间负1锛杩涓绱规瀛涓癸剧ず
                    int x = 7-i+w*8+x0;
                    int y = h+y0;
                    lcdDrawPoin(x,y,color);
                }
            }
        }  
    }
}
